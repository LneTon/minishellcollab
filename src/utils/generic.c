#include "../include/minishell.h"

int	min(int a, int b)
{
	if (a >= b)
		return (b);
	return (a);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
