size_t	ft_strlen(const char *str)
{
	size_t	count;

	if (str == NULL)
		return (0);
	count = 0;
	while (str[count] != '\0')
		count++;
	return (count);
}

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned int	i;
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *) s1;
	str2 = (unsigned char *) s2;
	i = 0;
	while ((str1[i] != '\0' || str2[i] != '\0') && i < n)
	{
		if (str1[i] != str2[i])
			return (str1[i] - str2[i]);
		i++;
	}
	return (0);
}

bool    is_same_string(char *str1, char *str2)
{
    if (!ft_strncmp(str1, str2, ft_strlen(str1)))
        return (true);
    else
        return (false);
}

int main()
{
    char *str1 = "(null)";
    char *str2 = "|";

    if (is_same_string(str1, str2))
        printf("same");
    else
        printf("different.");
}
